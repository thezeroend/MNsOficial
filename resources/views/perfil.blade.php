            <h2 id="historia">História</h2>

                <p>Nos anos 1970 e 1980, o conceito de marina, enquanto complexo de atividades com infra-estrutura náutica e de lazer, não havia se difundido no Brasil. Com exceção do Iate Clube de Santos e do Rio de Janeiro, existiam apenas garagens náuticas.

               A Marinas Nacionais foi a primeira marina brasileira e começou a operar após levantar um hangar com capacidade para empilhamento de 20 barcos.

               Em 1987, construiu a bacia, aumentando a capacidade de vagas. Finalmente, em 2012, novos investimentos expandiram a capacidade para mais de 760 vagas. A estrutura da Marinas Nacionais fornece diversos benefícios ao proprietário de barco: ampla área de estacionamento, segurança 24 horas, posto de combustível, restaurante, loja de conveniência, piscina, playground, saunas, vestiários, entre outros.</p>

